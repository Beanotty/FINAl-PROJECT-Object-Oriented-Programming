    private void buttonRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRegisterActionPerformed
        // TODO add your handling code here:
        String id = autonumber();
        String username = txtUname.getText().toString().trim();
        String password = txtPass.getText().toString().trim();
      
        if(password.equals("") || username.equals("")){
            JOptionPane.showMessageDialog(null, "Username or Password cannot be empty!");
            
        }
        else if (password.length() < 8){
            
            JOptionPane.showMessageDialog(null, "Password must be at least 8 characters");
        }
        else{
            try{
                String sql = "INSERT INTO usertable(`Username`, `Password`, `UserId`)  VALUES (?, ?, ?)";
                PreparedStatement masuk = con.prepareStatement(sql);
                masuk.setString(1, username);
                masuk.setString(2, password);
                masuk.setString(3, id);
                masuk.executeUpdate();
                masuk.close();
                JOptionPane.showMessageDialog(null, "Welcome to Fantatix, " + username + "!");
            } catch(SQLException e){
                e.printStackTrace();
            } finally{
                this.dispose();
                Login a = new Login();
                a.setVisible(true);
            }
        }
    }//GEN-LAST:event_buttonRegisterActionPerformed

