    public String autonumber(){
       String newID = "";
        try{
            
            String query = "SELECT * FROM usertable ORDER BY UserId DESC";
            ResultSet rs = con.executeQuery(query);
            
            if(rs.next()){
                String NoID = rs.getString(("UserId")).substring(1);
                String ID = "" + (Integer.parseInt(NoID) + 1);
                String Zero = "";
                
                if(ID.length() == 1) Zero = "000";
                else if(ID.length() == 2) Zero = "00";
                else if(ID.length() == 3) Zero = "0";
                else if(ID.length() == 4) Zero = "";
                
                newID = "U" + Zero + ID;
            } else{
                newID = "U0001";
            }
            rs.close();
        } catch(Exception e){
            System.out.println("autonumber error");
            e.printStackTrace();
        }
        return newID;
    }

